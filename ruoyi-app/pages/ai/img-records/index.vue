<script>
import { listImgRecords, delImgRecords } from '@/api/ai/imgRecords'

export default {
  data() {
    return {
      recordsList: [],
      searchKeyword: '',
      selectedBehavior: {},
      selectedDate: '',
      behaviorOptions: [
        { name: '全部行为', value: '' },
        { name: '写作', value: 'Writing' },
        { name: '阅读', value: 'Reading' },
        { name: '听讲', value: 'Listening' },
        { name: '睡觉', value: 'Sleeping' },
        { name: '讨论', value: 'Discussing' },
        { name: '举手', value: 'Raising_hand' },
        { name: '低头', value: 'Bowing' },
        { name: '玩手机', value: 'Phone' },
        { name: '站立', value: 'Stand' }
      ],
      queryParams: {
        pageNum: 1,
        pageSize: 10,
        username: '',
        label: '',
        startTime: ''
      },
      total: 0,
      hasMore: true,
      isLoading: false,
      currentDetail: {}
    }
  },
  onLoad() {
    this.fetchRecords()
  },
  onPullDownRefresh() {
    this.refreshData()
  },
  onReachBottom() {
    if (this.hasMore && !this.isLoading) {
      this.loadMore()
    }
  },
  methods: {
    fetchRecords(isRefresh = false) {
      if (isRefresh) {
        this.queryParams.pageNum = 1
        this.recordsList = []
        this.hasMore = true
      }
      
      if (!this.hasMore) return
      
      this.isLoading = true
      
      listImgRecords(this.queryParams).then(response => {
        const newRecords = response.data || []
        this.total = response.total || 0
        
        if (isRefresh) {
          this.recordsList = newRecords
        } else {
          this.recordsList = [...this.recordsList, ...newRecords]
        }
        
        // 判断是否还有更多数据
        this.hasMore = this.recordsList.length < this.total
        
        // 更新页码
        if (!isRefresh) {
          this.queryParams.pageNum++
        }
      }).catch(error => {
        console.error('获取记录失败:', error)
        uni.showToast({
          title: '获取记录失败',
          icon: 'none'
        })
      }).finally(() => {
        this.isLoading = false
        if (isRefresh) {
          uni.stopPullDownRefresh()
        }
      })
    },
    
    searchRecords() {
      this.queryParams.username = this.searchKeyword
      this.fetchRecords(true)
    },
    
    onBehaviorChange(e) {
      const index = e.detail.value
      this.selectedBehavior = this.behaviorOptions[index]
      this.queryParams.label = this.selectedBehavior.value
      this.fetchRecords(true)
    },
    
    onDateChange(e) {
      this.selectedDate = e.detail.value
      this.queryParams.startTime = e.detail.value
      this.fetchRecords(true)
    },
    
    loadMore() {
      if (!this.hasMore || this.isLoading) return
      this.queryParams.pageNum++
      this.fetchRecords()
    },
    
    refreshData() {
      this.fetchRecords(true)
    },
    
    deleteRecord(id) {
      uni.showModal({
        title: '确认删除',
        content: '确定要删除这条记录吗？',
        success: (res) => {
          if (res.confirm) {
            uni.showLoading({
              title: '删除中...'
            })
            
            delImgRecords(id).then(response => {
              uni.hideLoading()
              uni.showToast({
                title: '删除成功',
                icon: 'success'
              })
              this.refreshData()
            }).catch(error => {
              uni.hideLoading()
              console.error('删除失败:', error)
              uni.showToast({
                title: '删除失败',
                icon: 'none'
              })
            })
          }
        }
      })
    },
    
    formatLabel(label) {
      // 处理 JSON 字符串
      let labels = label
      if (typeof label === 'string') {
        try {
          labels = JSON.parse(label)
        } catch (e) {
          labels = [label]
        }
      }
      
      // 如果是数组，取第一个
      if (Array.isArray(labels)) {
        labels = labels[0] || label
      }
      
      const labelMap = {
        'Writing': '写字',
        'Reading': '阅读',
        'Listening': '听讲',
        'Sleeping': '睡觉',
        'Discussing': '讨论',
        'Raising_hand': '举手',
        'Bowing': '低头',
        'Phone': '玩手机',
        'Stand': '站立'
      }
      return labelMap[labels] || labels
    },
    
    formatLabelSummary(label) {
      // 处理 JSON 字符串
      let labels = label
      if (typeof label === 'string') {
        try {
          labels = JSON.parse(label)
        } catch (e) {
          labels = [label]
        }
      }
      
      if (!Array.isArray(labels)) {
        labels = [labels]
      }
      
      // 统计各个行为的数量
      const labelMap = {
        'Writing': '写字',
        'Reading': '阅读',
        'Listening': '听讲',
        'Sleeping': '睡觉',
        'Discussing': '讨论',
        'Raising_hand': '举手',
        'Bowing': '低头',
        'Phone': '玩手机',
        'Stand': '站立'
      }
      
      const counts = {}
      labels.forEach(l => {
        const translatedLabel = labelMap[l] || l
        counts[translatedLabel] = (counts[translatedLabel] || 0) + 1
      })
      
      // 生成摘要字符串，如 "写字×10, 阅读×5"
      const summary = Object.entries(counts)
        .map(([label, count]) => `${label}×${count}`)
        .join(', ')
      
      return summary || '无'
    },
    
    formatAverageConfidence(confidence) {
      // 处理 JSON 字符串
      let confs = confidence
      if (typeof confidence === 'string') {
        try {
          confs = JSON.parse(confidence)
        } catch (e) {
          confs = [confidence]
        }
      }
      
      if (!Array.isArray(confs)) {
        confs = [confs]
      }
      
      // 计算平均置信度
      const sum = confs.reduce((acc, c) => {
        const num = parseFloat(c)
        if (isNaN(num)) return acc
        // 如果数字大于1，说明已经是百分比形式
        return acc + (num > 1 ? num : num * 100)
      }, 0)
      
      const avg = sum / confs.length
      return isNaN(avg) ? 'N/A' : avg.toFixed(1) + '%'
    },
    
    formatLabelArray(label) {
      // 处理 JSON 字符串，返回标签数组
      let labels = label
      if (typeof label === 'string') {
        try {
          labels = JSON.parse(label)
        } catch (e) {
          labels = [label]
        }
      }
      
      if (!Array.isArray(labels)) {
        labels = [labels]
      }
      
      const labelMap = {
        'Writing': '写字',
        'Reading': '阅读',
        'Listening': '听讲',
        'Sleeping': '睡觉',
        'Discussing': '讨论',
        'Raising_hand': '举手',
        'Bowing': '低头',
        'Phone': '玩手机',
        'Stand': '站立'
      }
      
      return labels.map(l => labelMap[l] || l)
    },
    
    formatConfidenceByIndex(confidence, index) {
      // 获取指定索引的置信度
      let confs = confidence
      if (typeof confidence === 'string') {
        try {
          confs = JSON.parse(confidence)
        } catch (e) {
          confs = [confidence]
        }
      }
      
      if (!Array.isArray(confs)) {
        confs = [confs]
      }
      
      const conf = confs[index]
      const confNum = parseFloat(conf)
      if (isNaN(confNum)) {
        return 'N/A'
      }
      
      // 如果数字大于1，说明已经是百分比形式
      if (confNum > 1) {
        return confNum.toFixed(1) + '%'
      } else {
        return (confNum * 100).toFixed(1) + '%'
      }
    },
    
    formatConfidence(confidence) {
      // 处理 JSON 字符串
      let confs = confidence
      if (typeof confidence === 'string') {
        try {
          confs = JSON.parse(confidence)
        } catch (e) {
          confs = [confidence]
        }
      }
      
      // 如果是数组，取第一个
      if (Array.isArray(confs)) {
        confs = confs[0] || 0
      }
      
      // 确保是数字
      const confNum = parseFloat(confs)
      if (isNaN(confNum)) {
        return 'N/A'
      }
      
      // 如果数字大于1，说明已经是百分比形式（如 94.53），直接添加 %
      // 如果数字小于等于1，说明是小数形式（如 0.9453），需要乘以100
      if (confNum > 1) {
        return confNum.toFixed(1) + '%'
      } else {
        return (confNum * 100).toFixed(1) + '%'
      }
    },
    
    formatTime(time) {
      if (!time) return ''
      const date = new Date(time)
      const year = date.getFullYear()
      const month = String(date.getMonth() + 1).padStart(2, '0')
      const day = String(date.getDate()).padStart(2, '0')
      const hours = String(date.getHours()).padStart(2, '0')
      const minutes = String(date.getMinutes()).padStart(2, '0')
      return `${year}-${month}-${day} ${hours}:${minutes}`
    },
    
    viewDetail(item) {
      this.currentDetail = item
      this.$refs.detailPopup.open()
    },
    
    closeDetail() {
      this.$refs.detailPopup.close()
    },
    
    previewImage(url) {
      uni.previewImage({
        urls: [url],
        current: url
      })
    }
  }
}
</script>

